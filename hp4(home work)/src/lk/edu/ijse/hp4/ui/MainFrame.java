    private void customerBtn1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_customerBtn1MouseClicked
        String custName=custNametxt.getText();
        String address=custAddressTxt.getText();
        String tel=custTelTxt.getText();
        List<CustomerDto>list=new ArrayList<>();
        int id=0;
        boolean telCheck=false;
        try {
           telCheck =vc.setTelNumber(tel);
        } catch (Exception ex) {
            Logger.getLogger(MainFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        if(telCheck){
      if(!custName.equals("")&& !address.equals("") && !tel.equals("")){
        CustomerDto customerDto=new CustomerDto(custName,address,tel);
        try {
            boolean b=customerControler.addCustomer(customerDto);
            
            if (b) {
                JOptionPane.showMessageDialog(null," A New Customer Added ");
                list =customerControler.getAllCustomer();
                for (CustomerDto customerDto1 : list) {
                    id=customerDto1.getId();
                }
                jComboBox3.removeAllItems();
                jComboBox3.addItem(""+id);
                custNametxt.setText("");
                custAddressTxt.setText("");
                custTelTxt.setText("");
                jComboBox2.requestFocus();
                
                //
            }else{
                JOptionPane.showMessageDialog(null," A New Customer Not Added !","",JOptionPane.WARNING_MESSAGE);
                loadCustomer();
            }
        } catch (Exception ex) {
            Logger.getLogger(MainFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
      }
      else{
           JOptionPane.showMessageDialog(null," please fill all details !","",JOptionPane.WARNING_MESSAGE);
           custNametxt.requestFocus();
      }
        }else{
           JOptionPane.showMessageDialog(null," please Check the tel Number !","",JOptionPane.WARNING_MESSAGE); 
        }
    }//GEN-LAST:event_customerBtn1MouseClicked

